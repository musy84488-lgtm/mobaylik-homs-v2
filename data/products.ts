export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getProductsByCategory(categoryId: string): Product[] {
  return products.filter((p) => p.categoryId === categoryId);
}

export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.isFeatured);
}

export function getNewProducts(): Product[] {
  return products.filter((p) => p.isNew);
}

export function getBestSellers(): Product[] {
  return products.filter((p) => p.isBestSeller);
}

export function getDiscountedProducts(): Product[] {
  return products.filter((p) => p.discount && p.discount > 0);
}

export function searchProducts(query: string): Product[] {
  const lowerQuery = query.toLowerCase();
  return products.filter(
    (p) =>
      p.name.toLowerCase().includes(lowerQuery) ||
      p.brand.toLowerCase().includes(lowerQuery) ||
      p.model.toLowerCase().includes(lowerQuery) ||
      p.description.toLowerCase().includes(lowerQuery) ||
      p.tags?.some((t) => t.toLowerCase().includes(lowerQuery))
  );
}

export function getAllBrands(): string[] {
  return [...new Set(products.map((p) => p.brand))];
}

export function getAllStorageOptions(): string[] {
  return [...new Set(products.map((p) => p.storage).filter(Boolean))];
}

export function getAllRamOptions(): string[] {
  return [...new Set(products.map((p) => p.ram).filter(Boolean))];
}
